"""
[실습1] 단어를 입력 받은 후 아래 코드를 작성하세요.
    - 입력 받은 단어가 알파벳만으로 구성되어 있는지 검사
    - 입력 받은 단어가 숫자만으로 구성되어 있는지 검사
    - 입력 받은 단어가 모두 대문자로만 구성되어 있는지 검사
    - 입력 받은 단어가 모두 소문자로만 구성되어 있는지 검사
    - 입력 받은 단어가 이메일 주소인지 검사
"""
word = input("단어를 입력하세요: ")
print(f'{word}는 알파벳만으로 구성되어있는가 : {word.isalpha()}')
print(f'{word}는 숫자만으로 구성되어있는가 : {word.isdecimal()}')
print(f'{word}는 모두 대문자만으로 구성되어있는가 : {word.isupper()}')
print(f'{word}는 모두 소문자만으로 구성되어있는가 : {word.islower()}')



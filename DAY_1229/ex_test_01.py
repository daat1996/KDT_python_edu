# [실습2] 파일명을 입력 받은 후 아래 코드를 작성하세요.  => str 전용 함수인 메서드 활용
#   - 입력 받은 파일이 text 파일인지 검사    =>  확장자 txt
#   - 입력 받은 파일이 jpg 파일인지 검사     =>  확장자 jpg
#   - 입력 받은 파일이 py 파일인지 검사      =>  확장자 py
# ---------------------------------------------------------

file = input("파일명을 입력하세요: ")
print(f'파일은 text 파일인가: {file.endswith("txt")}')
print(f'파일은 jpg 파일인가: {file.endswith("jpg")}')
print(f'파일은 py 파일인가: {file.endswith("py")}')